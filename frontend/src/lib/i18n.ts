export type Translations = typeof translations.en

function detectLocale(): Locale {
  const stored = localStorage.getItem('locale') as Locale | null
  if (stored === 'en' || stored === 'zh') return stored

  const lang = navigator.language || ''
  if (lang.startsWith('zh')) return 'zh'
  return 'en'
}

export function getStoredLocale(): Locale {
  return detectLocale()
}

export function setStoredLocale(locale: Locale) {
  localStorage.setItem('locale', locale)
}

/** Translate a backend SSH error message to the current locale. Falls back to original. */
export function translateSshError(msg: string, t: typeof translations.en): string {
  const lower = msg.toLowerCase()
  // More specific patterns first
  if (lower.includes('passwordless login still failed') || lower.includes('key authentication failed')) {
    return t.sshErrors.verifyFailed
  }
  if (lower.includes('passwordless ssh is not configured') || lower.includes('please provide the login password')) {
    return t.sshErrors.passwordlessNotConfigured
  }
  if (lower.includes('failed to install ssh key')) {
    return t.sshErrors.keyInstallFailed
  }
  if (lower.includes('cannot connect') || lower.includes('connection refused') || lower.includes('timed out') || lower.includes('no route') || lower.includes('nodename nor servname')) {
    return t.sshErrors.connectFailed
  }
  if (lower.includes('password authentication failed') || lower.includes('permission denied') || lower.includes('authentication')) {
    return t.sshErrors.authFailed
  }
  return msg
}
